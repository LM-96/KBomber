package kbomber.reflection.os

/**
 * Represents an *Operating System*
 */
enum class OperatingSystem {
    /**
     * Windows operating system
     */
    WINDOWS,

    /**
     * Unix/Linux operating system
     */
    LINUX,

    /**
     * MAC OSX operating system
     */
    MAC,

    /**
     * Solaris operating system
     */
    SOLARIS
}